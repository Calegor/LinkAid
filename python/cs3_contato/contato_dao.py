from db import conectar

#verifica se a tabela contato existe, senão, cria ela
def criar_tabela_contato():
    conexao = conectar()
    if not conexao:
        return

    cursor = None
    try:
        cursor = conexao.cursor()

        cursor.execute("""
            SELECT COUNT(*)
            FROM USER_TABLES
            WHERE TABLE_NAME = 'CONTATO'
        """)
        resultado = cursor.fetchone()

        if resultado and resultado[0] > 0:
            print("A tabela contato já existe.")
            return

        cursor.execute("""
            CREATE TABLE CONTATO (
                ID_CONTATO NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
                TP_CONTATO VARCHAR2(20) NOT NULL,
                NM_CONTATO VARCHAR2(100) NOT NULL,
                TP_DOCUMENTO VARCHAR2(20) NOT NULL,
                NR_DOCUMENTO VARCHAR2(30) NOT NULL,
                DS_EMAIL VARCHAR2(100),
                NR_CEP VARCHAR2(8),
                NR_CRO VARCHAR2(30),
                DS_ESPECIALIDADE VARCHAR2(100)
            )
        """)

        conexao.commit()
        print("Tabela CONTATO criada com sucesso.")

    except Exception as e:
        print(f"Erro ao verificar/criar tabela CONTATO: {e}")
    finally:
        if cursor:
            cursor.close()
        conexao.close()

#insere um contato na tabela
