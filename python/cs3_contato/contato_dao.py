#modifica um contato na tabela
def atualizar_contato(id_contato, tipo_contato, nome, tipo_documento, numero_documento, email, cep, cro="", especialidade=""):
    conexao = conectar()
    if not conexao:
        return

    cursor = None
    try:
        cursor = conexao.cursor()
        sql = """
            UPDATE CONTATO
            SET TP_CONTATO = :1,
                NM_CONTATO = :2,
                TP_DOCUMENTO = :3,
                NR_DOCUMENTO = :4,
                DS_EMAIL = :5,
                NR_CEP = :6,
                NR_CRO = :7,
                DS_ESPECIALIDADE = :8
            WHERE ID_CONTATO = :9
        """
        cursor.execute(sql, (
            tipo_contato,
            nome,
            tipo_documento,
            numero_documento,
            email,
            cep,
            cro,
            especialidade,
            id_contato
        ))
        conexao.commit()

        if cursor.rowcount > 0:
            print("Contato atualizado com sucesso.")
        else:
            print("Contato não encontrado.")
    except Exception as e:
        print(f"Erro ao atualizar contato: {e}")
    finally:
        if cursor:
            cursor.close()
        conexao.close()

