#lista todos os contatos de um tipo na tabela
def listar_contatos(tipo_contato):
    conexao = conectar()
    if not conexao:
        return []

    cursor = None
    try:
        cursor = conexao.cursor()
        cursor.execute("""
            SELECT ID_CONTATO, TP_CONTATO, NM_CONTATO, TP_DOCUMENTO, NR_DOCUMENTO, DS_EMAIL,
                   NR_CEP, NR_CRO, DS_ESPECIALIDADE
            FROM CONTATO
            WHERE UPPER(TP_CONTATO) = UPPER(:1)
            ORDER BY ID_CONTATO
        """, (tipo_contato,))
        resultados = cursor.fetchall()

        contatos = []
        for row in resultados:
            contatos.append({
                "id_contato": row[0],
                "tp_contato": row[1],
                "nm_contato": row[2],
                "tp_documento": row[3],
                "nr_documento": row[4],
                "ds_email": row[5],
                "nr_cep": row[6],
                "nr_cro": row[7],
                "ds_especialidade": row[8]
            })

        return contatos
    except Exception as e:
        print(f"Erro ao listar contatos: {e}")
        return []
    finally:
        if cursor:
            cursor.close()
        conexao.close()

